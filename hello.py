x = 20
print(x)
print(type(x))

x = 20.5
print(x)
print(type(x))

x = 1j
print(x)
print(type(x))

x = ["apple","banana","mango"]
print(x)
print(type(x))

x = ("apple","banana","mango")
print(x)
print(type(x))

x = range(6)
print(x)
print(type(x))

x = {"name":"john","age":18}
print(x)
print(type(x))

x = {"apple","banana","mango"}
print(x)
print(type(x))

x = frozenset({"apple","banana","mango"})
